export function matchCompany(company) {
    switch (company){
        case 'sncf':
           return "SNCF";
        case 'airfrance':
           return "Air France";
        case 'delta':
           return "Delta";
        case 'lufthansa':
            return "Lufthansa"
        case 'klm':
            return "KLM"
        case 'aircaraibes':
            return "Air Caraibes"
        case 'ryanair':
            return "Ryanair"
        case 'qatar':
            return "Qatar Airways"
        case 'british':
            return "British Airways"
        case 'emirates':
            return "Emirates"
        case 'aa':
            return "American Airlines"
        case 'ua':
            return "United Airlines"
        case 'aeroflot':
            return "Aeroflot"
        case 'westjet':
            return "Westjet"
    }
}